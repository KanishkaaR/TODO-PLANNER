package bean;

public class User {
	String user_name;
	int user_id;
	String user_psw;
	public int getUser_id() {
		return user_id;
	}
	public void setUser_id(int i) {
		user_id = i;
	}
	public String getUser_name() {
		return user_name;
	}
	public void setUser_name(String s) {
		user_name=s;
	}
	public String getUser_psw() {
		return user_psw;
	}
	public void setUser_psw(String s) {
		user_psw=s;
	}
}
